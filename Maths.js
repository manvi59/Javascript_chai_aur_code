//  Math is a object which contain two many propeties

console.log(Math); // Object [Math] {}  which contain many methods

console.log(Math.abs(-4)); // 4 convert negative in positive value
console.log(Math.round(4.3)); //4

console.log(Math.round(4.6)); // 5

console.log(Math.ceil(4.2)); // 5 upper value

console.log(Math.floor(4.2)); // 4 lower value

console.log(Math.min(2, 4, 5, 7)); // 2

console.log(Math.max(3, 999, 5, 7, 9)); // 999

console.log(Math.random()); // 0.8195180256145493   it will give the value between 0 and 1

console.log(Math.random() * 10); // 0.7627802912429482
console.log(Math.random() * 10 + 1); // for avoiding 0.01 the we add 1 to avoiding   // 10.502760400213312
